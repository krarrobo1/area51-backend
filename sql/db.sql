Sentencia para crear tabla de empresa.
Ubicacion es un tipo de dato point '(-4.0108032,-79.20599039999999)'
Estado indica si la empresa cuenta con el servicio activo.
true para activo, false inactiva.
*/

CREATE TABLE IF NOT EXISTS empresas
(
id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
nombre VARCHAR NOT NULL UNIQUE,
latitud NUMERIC(9,6)
longitud NUMERIC(9,6),
estado BOOLEAN DEFAULT true NOT NULL
);

